#include <iostream>

int main() {
    int age;

    // Prompt user to enter their age
    std::cout << "Enter your age: ";
    std::cin >> age;

    // Check if age is greater than or equal to 18
    if (age >= 18) {
        std::cout << "You are an adult." << std::endl;
    } else {
        std::cout << "You are a minor." << std::endl;
    }

    return 0;
}
